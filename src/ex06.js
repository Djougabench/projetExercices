const add = (nombre1, nombre2) => {
    return nombre1 + nombre2
}

const sub = (nombre1, nombre2) => {
    return nombre1 - nombre2
}

const mul = (nombre1, nombre2) => {
    return nombre1 * nombre2
}

const div = (nombre1, nombre2) => {
    return nombre1 / 2
}

console.log(add(10, 11))
console.log(sub(6, 4))
console.log(mul(4, 5))
console.log(div(10, 2))
